<h2>Envíos</h2>
<h3>1. Información de envío</h3>
<p>Todos los pedidos serán enviados por Agencia de Transportes mediante la modalidad estándar, con un tiempo de entrega
    de<b> 48/72 h en días hábiles </b>o la modalidad urgente, con un tiempo de entrega
    de <b>24 h en días hábiles</b>. Consideramos día hábil los días laborables de lunes a viernes,
    exceptuando los festivos oficiales marcados en el calendario oficial.</p>
<h3>2. Zonas de envío</h3>
<p>Solo servimos pedidos a <b>España Peninsular y Baleares</b>.Actualmente no realizamos envíos a las
    Islas Canarias, Ceuta y Melilla, así como tampoco a apartados de correos. Esperamos poder ampliar en breve nuestros
    puntos de destino.</p>
<h3>3. Entrega de pedidos</h3>
<p>La entrega de los pedidos se realizará en el domicilio de entrega designado libremente por el cliente. De tal forma,
    no asumimos responsabilidad alguna cuando la entrega del producto o servicio no llegue porque los datos facilitados
    por el usuario sean falsos, inexactos o incompletos o cuando la entrega no pueda efectuarse por causas ajenas a la
    empresa de envíos, asignada para tal efecto, como lo es la ausencia del destinatario.</p>
<p>Sin perjuicio de lo anterior, nos comprometemos a adoptar las medidas exigidas a un comerciante diligente para que la
    entrega pueda efectuarse en el tiempo acordado, y de no ser así, lo antes posible, a satisfacción del remitente como
    del destinatario, por lo que no podrá imputársenos responsabilidad alguna.</p>
<h2>Devoluciones</h2>
<p>El reembolso se realizará directamente a la tarjeta de crédito /débito o PayPal usada en el pedido original en el
    plazo de 30 días, una vez inspeccionado el artículo devuelto y  comprobado que se encuentra en correctas
    condiciones.</p>
<h3>1. Productos devueltos por desistimiento del consumidor </h3>
<p>El cliente dispondrá de un plazo de 14 días hábiles contados a partir de la fecha de recepción del producto para la
    devolución del mismo. Salvo que la devolución sea realizada por defectos en el producto, los gastos relativos al
    envío serán asumidos por parte del usuario. En todo caso el producto deberá ser devuelto en su embalaje original y
    en perfecto estado.</p>
<p><b>Centro de Servicio de Devoluciones:<br><br>Avd. de Esteiro 121-123 1º K  15404 Ferrol, A Coruña, España<br></b></p>
<p>Como justificante de tu devolución, te recomendamos que siempre guardes el recibo del mensajero como comprobante de
    entrega de tu paquete. El coste de la devolución corre a cargo del cliente.</p>
<h3>2. Productos Defectuosos</h3>
<p>Hacemos todo lo posible para que nuestros productos se suministren en condiciones óptimas. Si un producto tiene un
    defecto de origen, o de alguna manera no es conforme en los términos establecidos en la normativa vigente de
    Consumo, realizaremos su sustitución gratuita o reembolsaremos el valor del producto si éste ya no se encuentra en
    stock, incluyendo los costes de la devolución por parte del cliente.</p>
<p>En el caso de artículos defectuosos debido a un defecto de fabricación, FerroFarma remplazará el artículo
    por uno nuevo de idénticas características.</p>
<p>En todo caso el producto deberá ser devuelto en su embalaje original y en el mismo estado de recepción.</p>
<p><b>Centro de Servicio de Devoluciones:<br><br>Avd. de Esteiro 121-123 1º K  15404 Ferrol, A Coruña, España<br></b></p>
<p>Como justificante de tu devolución, te recomendamos que siempre guardes el recibo del mensajero como comprobante de
    entrega de tu paquete.</p>
<p>Si detectas alguna incidencia en la recepción de tu pedido, ponte en contacto con el Servicio de atención al Cliente
