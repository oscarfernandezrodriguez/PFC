        envío,</b> los cuales aparecerán en pantalla, se podrá realizar mediante transferencia bancaria. <br>
    Para proceder al pago, el Usuario deberá seguir las instrucciones que aparecen en pantalla. proporcionando la
    siguiente información:</p>
<p><b>3.2 La empresa se obliga a entregar los bienes comprados por el Usuario, en
        el domicilio del comprador señalado a tal efecto en el formulario de pedido (no se entregarán productos en
