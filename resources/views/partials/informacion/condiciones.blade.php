<p><b>9.2 En el caso de que se produzca cualquier tipo de discrepancia o
        reclamación entre las partes</b> en relación con el cumplimiento o el contenido de las presentes
    Condiciones Generales, las partes negociarán de buena fe para intentar resolver tal discrepancia o reclamación
    dentro del plazo máximo de un (1) mes contado a partir de la fecha en que cualquiera de ellas notifique
    formalmente a la otra que ha surgido la discrepancia o notifique la reclamación. <br> Para el supuesto de que la
    discrepancia o la reclamación no se resolviera dentro del plazo máximo señalado, las partes, con renuncia
    expresa a cualquier fuero que pudiera corresponderles, someten la decisión del asunto planteado al Arbitraje
    Institución de la Asociación Española de Arbitraje Tecnológico (ARBITEC), a la que se encomienda la
    administración del arbitraje y la designación de los árbitros de acuerdo con su Reglamento y Estatutos.<br> <br>
    Igualmente las partes se obligan a aceptar y cumplir la decisión contenida en el laudo arbitral que se dicte. Si
    el arbitraje no llegara a realizarse de mutuo acuerdo o fuera declarado nulo, ambas partes se someten a los
    Juzgados y Tribunales, con renuncia expresa a su propio fuero, si éste fuera otro.</p>
