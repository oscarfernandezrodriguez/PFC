    la fecha de formulación del pedido. Los gastos de envío de los objetos comprados, serán abonados por el
    Comprador.</p>
<h5>IV. Política de protección de datos de carácter personal
</h5>
<p><b>4.1 A los efectos de lo dispuesto en la Ley Orgánica 15/1999,</b> de 13
    de diciembre, de Protección de Datos de Carácter Personal, la empresa informa al Usuario de la existencia de un
    fichero automatizado de datos de carácter personal creado con los datos obtenidos en el Portal por y para la
    empresa, y bajo su responsabilidad, con las finalidades de información y comercialización de los productos
    ofrecidos en el Portal, así como la realización de actividades promocionales y publicitarias que puedan ser de
    su interés, para ampliar y mejorar nuestros Productos adecuando nuestras ofertas a sus preferencias o
    necesidades y para permitir una navegación personalizada.</p>
<p><b>4.2 El usuario acepta expresamente la inclusión de los datos recabados
        durante la navegación por el Portal,</b> o proporcionados mediante la cumplimentación de cualquier
    formulario, así como los derivados de la relación comercial y/o entrega de los productos comprados, en el
    fichero automatizado de datos de carácter personal referido en el apartado primero. Durante el proceso de
    recogida de los datos, el Usuario será informado del carácter obligatorio o no de recogida de tales datos para
    la entrega de los Productos, excepto para la realización de pedidos en cuyo caso la empresa informa desde este
    momento al Usuario que no se considerará de carácter voluntario, por lo que será necesario su inclusión para la
    formalización del pedido de compra, proporcionar los datos relativos a la identidad, domicilio y número de
    cuenta bancaria para realizar el pago, al ser estos datos necesarios para la formalización y conclusión de la
    venta.</p>
<p><b>4.3 El Usuario podrá ejercitar,</b> respecto de los datos recabados en
    la forma prevista en el apartado anterior, los derechos reconocidos en la Ley Orgánica 15/1999, y en particular
    los derechos de acceso, rectificación o cancelación de datos y oposición, si resultase pertinente, así como el
    de revocación del consentimiento para la cesión de sus datos. Los derechos referidos en el párrafo anterior
    podrán ser ejercitados por el Usuario, mediante solicitud escrita y firmada dirigida mediante el envío de un
    correo electrónico.</p>
<p><b>4.4 Mediante la aceptación de estas condiciones,</b> el Usuario
    consiente expresamente la cesión o comunicación de los datos incluidos en el fichero referido en el apartado
    4.1, a las empresas que estén situadas o no en el territorio español, e incluso en el último caso a empresas que
    puedan estar sometidas a un régimen de no protección que no fuera equiparable al que proporciona la ley
    española, y con la finalidad de que estas compañías puedan tratar sus datos de carácter personal para hacerle
    llegar ofertas y mensajes publicitarios.</p>
<p><b>4.5 La empresa se compromete, en la utilización de los datos incluidos en el
        fichero, a respetar su confidencialidad y a utilizarlos de acuerdo con la finalidad del fichero,</b>
    así como a dar cumplimiento a su obligación de guardarlos y a adoptar todas las medidas para evitar la
    alteración, pérdida, tratamiento o acceso no autorizado, de acuerdo con lo establecido en el Reglamento de
    Medidas de Seguridad de los ficheros automatizados que contengan datos de carácter personal, aprobado por el
    Real Decreto 994/1999, de 11 de julio.</p>
