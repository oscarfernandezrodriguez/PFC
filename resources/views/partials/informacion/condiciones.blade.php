<h1 id="cabeceraInfo">CONDICIONES GENERALES DE VENTA</h1>
<br>
<h5>I. Condiciones generales de venta</h5>
<p><b>1.1 Las presentes condiciones generales de contratación,</b> junto con
    las condiciones particulares que puedan establecerse, regularán expresamente las relaciones surgidas entre
    nuestra empresa (de aquí en adelante la empresa <b><u>FerroFarma</u></b>)
    y los terceros (en adelante "Usuarios") que utilicen el portal de Internet (de aquí en adelante el "Portal"),
    así como con los terceros que contraten los productos ofrecidos a través del mismo.</p>
<p>La empresa se reserva, en todo momento, el derecho a modificar unilateralmente, sin
    aviso previo, y en cualquier momento la presentación y configuración del Portal.</p>
<p><b>1.2 Las presentes Condiciones Generales han sido elaboradas de conformidad
        con lo establecido en la Ley 7/1998 sobre Condiciones Generales de la contratación,</b> el Real Decreto
    1906/1999 por el que se regula la contratación telefónica o electrónica con condiciones generales en desarrollo
    del artículo 5.3 de la Ley 7/1998, la Ley 26/1984 General para la Defensa de Consumidores y Usuarios, la Ley
    7/1996 de Ordenación del Comercio Minorista, el Real Decreto-Ley 14/1999 reguladora de la Firma Electrónica y
    cuantas disposiciones legales resulten de aplicación.</p>
<p><b>1.3 Por la utilización del Portal Vd. adquiere la condición de
        Usuario. </b>Tanto la visita como la utilización y/o la adquisición de cualquiera de los productos del
    Portal, suponen la aceptación como Usuario, sin reservas de ninguna clase, a todas y cada una de Condiciones
    Generales, así como en su caso, a las Condiciones Particulares que, en su caso, rijan la adquisición.</p>
<p><b>1.4 La empresa podrá, en todo momento y sin previo aviso, modificar las
        presentes Condiciones Generales,</b> mediante la publicación de dichas modificaciones en el Portal con
    el fin de que puedan ser conocidas por los Usuarios siempre antes de la visita al Portal o a la adquisición de
    cualesquiera bienes ofertados en el Portal.</p>
<h5>II. Productos ofrecidos. Sistema de compra
</h5>
<p><b>2.1 Los productos ofertados en el Portal, junto con sus características y
        precios aparecerán en pantalla.</b><br> Asimismo aparecerán en pantalla los gastos de envío al
    domicilio del Usuario de los productos, que serán siempre por cuenta del comprador. Dichos productos estarán
    disponibles para su venta hasta fin de existencias. Los precios indicados en pantalla son en euros e incluyen el
    I.V.A. y cualquier otro impuesto que fuera de aplicación y son los vigentes, salvo error mecanográfico.<br> <br>
    Las ofertas estarán debidamente indicadas en pantalla se reserva el derecho a decidir, en cada momento, los
    productos que se contengan y se ofrezcan a los Usuarios a través del Portal. De este modo podrá en cualquier
    momento añadir nuevos productos a los incluidos en el Portal, entendiéndose, salvo que se disponga otra cosa,
    que tales nuevos productos se regirán por lo dispuesto en las Condiciones Generales en vigor en ese momento.
    <br> Asimismo, la empresa se reserva el derecho a dejar de facilitar el acceso, en cualquier momento y sin
    previo aviso, a cualquiera de los productos ofrecidos en el Portal.</p>
<p><b>2.2. Para la adquisición de los productos ofertados en el Portal solicitará
        a los Usuarios que procedan a registrarse,</b> para lo cual dichos Usuarios, que deberán ser mayores de
    dieciocho (18) años, deberán cumplimentar las instrucciones que aparezcan en pantalla y que requerirán ejecutar
    las siguientes instrucciones: <br> <br> - Cumplimentación completa del formulario electrónico que en cada
    momento aparezca en el Portal siguiendo las instrucciones indicadas en el mismo. <br> <br> - Pulsación del botón
    "Aceptar". <br> <br> - Recepción en la cuenta de correo electrónico del Usuario del "Nombre de Usuario" y
    "Contraseña". <br> <br> El Nombre de Usuario y la Contraseña tienen carácter personal e intransferible. La
    empresa podrá efectuar, con el adecuado preaviso, modificaciones en el nombre del usuario y/o contraseña, en
    cuyo caso las claves modificadas perderán su validez.</p>
<p><b>2.3 Una vez registrado el Usuario, y para proceder a la compra de
        Productos,</b> deberá añadir el producto que desea en la Cesta de Compra, según las indicaciones
    recogidas en pantalla, cumplimentando a estos efectos el formulario de pedido suministrado y enviándolo a
    nuestro correo, lo cual supone la lectura y aceptación de todas las presentes Condiciones Generales, así como en
    su caso las Condiciones Particulares existentes.<br> La compra se entenderá efectuada.</p>
<p><b>2.4 Una vez efectuada la compra y en el plazo más breve posible, siempre antes de que transcurran treinta
        (30) días</b> desde la ejecución de la compra, el Departamento de Atención al Cliente de la empresa
    remitirá al Usuario comprador al correo electrónico señalado en el formulario la correspondiente factura. La
    confirmación de pedido remitida por la empresa no tendrá validez como factura, sólo como comprobante de compra.
</p>
