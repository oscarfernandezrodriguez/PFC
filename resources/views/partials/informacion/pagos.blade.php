<h2>Contrareembolso</h2>
<p >El pago contrareembolso es un medio de pago utilizado en las ventas por Internet que
    consiste en <b> abonar el coste del producto comprado</b> , con un recargo fijo de 5€ (IVA Incluído) por
    gastos de gestión de cobro, <b> directamente a la empresa de transporte que realiza la entrega del
        pedido</b> en el domicilio del cliente, en efectivo (moneda y billetes).<br><br><b> Condiciones del
        pago contrareembolso:</b> recargo <b> 5€</b> .</p>
