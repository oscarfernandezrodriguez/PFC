<h2>Tarjeta de crédito/débito</h2>
<p >La tienda "FERROFARMA" acepta: <b> Mastercard</b> ,<b> Visa
        crédito</b> , <b> Visa débito</b> a través de la pasarela de pago seguro de A Banca<b> .</b></p>
<p >El importe total de su compra incluidos los gastos de envío y el IVA será cargado en su
    tarjeta de crédito en el momento de concluir el proceso de realización de pedido. Posteriormente se enviará una
    confirmación de aceptación de pedido. Si cancela su pedido antes de que haya sido enviado el producto, procederemos
    al abono del cargo.</p>
<p >El pago con tarjeta de crédito/débito le garantiza una forma de <b> pago simple
        y segura</b> . Sus datos no se registrarán en ninguna base de datos sino que viajarán encriptados
    directamente al <b> TPV Virtual</b> del Banco, usando la <b> tecnología
        SSL </b> (protocolo criptográfico empleado para realizar una conexión segura entre su ordenador y la
    TPV Virtual de nuestro banco). Por ello,<b> los datos de su tarjeta están a salvo y sólo los conoce el
        banco. </b></p>
